// Aithmetic operator

// Addition
var a = 10;
var b = 20;
var sum = a + b;
console.log(sum);

// subtraction
var s = 80;
var t = 30;
var result = s - t;
console.log(result);

// Multiplication
var price = 40;
var quantity = 20;
var total = price * quantity;
console.log(total);


// Division
var totalMarks = 100;
var students = 4;
var average = totalMarks / students;
console.log(average);


// Modulus
var number = 10;
var remainder = number % 3;

console.log(remainder);

// reassigning with arithmetics
var score = 10;
score = score + 5;
console.log(score);

// this is the shorthand of the above same output
var score = 10;
score += 5;
console.log(score);
 
